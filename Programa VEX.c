task usercontrol()
{
	while(1)
	{
		movement();
		arm();
		if(vexRT[Btn5U] == 1)
		{
			motor[3] = 250;
			motor[4] = -250;
		}

		if(vexRT[Btn5D] == 1)
		{
			motor[3] = -250;
			motor[4] = 250;
		}

		if(vexRT[Btn6U] == 1)
		{
			motor[7] = percent(50);
		}

		if(vexRT[Btn6D] == 1)
		{
			motor[7] = percent(-100);
		}


		sleep(100);
		allInOff();
	}
}

